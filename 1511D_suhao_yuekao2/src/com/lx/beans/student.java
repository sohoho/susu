package com.lx.beans;

public class student {
     private int id;
     private String kec;
     private String name;
     private String riqi;
     private kec kec2;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getKec() {
		return kec;
	}
	public void setKec(String kec) {
		this.kec = kec;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRiqi() {
		return riqi;
	}
	public void setRiqi(String riqi) {
		this.riqi = riqi;
	}
	public kec getKec2() {
		return kec2;
	}
	public void setKec2(kec kec2) {
		this.kec2 = kec2;
	}
     
}
